CREATE TABLE HOSPITAL (
    HOSPITAL_ID     CHAR(6)      PRIMARY KEY NOT NULL,
    HOSPITAL_NAME   VARCHAR(100) NOT NULL,
    HOSPITAL_PHONE  VARCHAR(20)  NOT NULL,
    HOSPITAL_EMAIL  VARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE DEPARTMENT (
    DEPARTMENT_ID    CHAR(6)      PRIMARY KEY NOT NULL,
    DEPARTMENT_NAME  VARCHAR(100) NOT NULL,
    DEPARTMENT_TYPE  VARCHAR(50)  NOT NULL,
    DEPARTMENT_PHONE VARCHAR(20)  NOT NULL,
    HOSPITAL_ID      CHAR(6)      NOT NULL,
    FOREIGN KEY (HOSPITAL_ID) REFERENCES HOSPITAL(HOSPITAL_ID)
);

CREATE TABLE WARD (
    WARD_ID       CHAR(6)      PRIMARY KEY NOT NULL,
    WARD_NAME     VARCHAR(100) NOT NULL,
    WARD_PHONE    VARCHAR(20)  NOT NULL,
    DEPARTMENT_ID CHAR(6)      NOT NULL,
    FOREIGN KEY (DEPARTMENT_ID) REFERENCES DEPARTMENT(DEPARTMENT_ID)
);

CREATE TABLE DOCTOR (
    DOCTOR_ID     CHAR(6)      PRIMARY KEY NOT NULL,
    DOCTOR_NAME   VARCHAR(100) NOT NULL,
    DATE_OF_BIRTH DATE         NOT NULL,
    GENDER        CHAR(1)      NOT NULL CHECK (GENDER IN ('M', 'F', 'O')),
    DEPARTMENT_ID CHAR(6)      NOT NULL,
    FOREIGN KEY (DEPARTMENT_ID) REFERENCES DEPARTMENT(DEPARTMENT_ID)
);

CREATE TABLE PHARMACY (
    PHARMACY_ID   CHAR(6)      PRIMARY KEY NOT NULL,
    PHARMACY_NAME VARCHAR(100) NOT NULL
);

CREATE TABLE PATIENT (
    PATIENT_ID   CHAR(6)      PRIMARY KEY NOT NULL,
    PATIENT_NAME VARCHAR(100) NOT NULL,
    BIRTH_DATE   DATE         NOT NULL,
    GENDER       CHAR(1)      NOT NULL CHECK (GENDER IN ('M', 'F', 'O')),
    WARD_ID      CHAR(6)      NOT NULL,
    FOREIGN KEY (WARD_ID) REFERENCES WARD(WARD_ID)
);

CREATE TABLE PRESCRIPTION (
    PRESCRIPTION_ID CHAR(6)      PRIMARY KEY NOT NULL,
    MEDICATION_NAME VARCHAR(100) NOT NULL,
    MEDICATION_TYPE VARCHAR(50)  NOT NULL,
    PHARMACY_ID     CHAR(6)      NOT NULL,
    FOREIGN KEY (PHARMACY_ID) REFERENCES PHARMACY(PHARMACY_ID)
);

CREATE TABLE APPOINTMENT (
    APPOINTMENT_ID     CHAR(6)     PRIMARY KEY NOT NULL,
    APPOINTMENT_DATE   DATE        NOT NULL,
    APPOINTMENT_TIME   TIME        NOT NULL,
    APPOINTMENT_STATUS VARCHAR(20) NOT NULL CHECK (APPOINTMENT_STATUS IN ('Scheduled', 'Completed', 'Cancelled')),
    PATIENT_ID         CHAR(6)     NOT NULL,
    DOCTOR_ID          CHAR(6)     NOT NULL,
    FOREIGN KEY (PATIENT_ID) REFERENCES PATIENT(PATIENT_ID),
    FOREIGN KEY (DOCTOR_ID)  REFERENCES DOCTOR(DOCTOR_ID)
);

CREATE TABLE MEDICAL_RECORD (
    MEDICAL_RECORD_ID CHAR(6)      PRIMARY KEY NOT NULL,
    DIAGNOSIS         VARCHAR(255) NOT NULL,
    APPOINTMENT_ID    CHAR(6)      NOT NULL UNIQUE,
    FOREIGN KEY (APPOINTMENT_ID) REFERENCES APPOINTMENT(APPOINTMENT_ID)
);

CREATE TABLE MEDICAL_RECORD_PRESCRIPTION (
    RECORD_ID         CHAR(6) PRIMARY KEY NOT NULL,
    RECORD_DATE       DATE    NOT NULL,
    MEDICAL_RECORD_ID CHAR(6) NOT NULL,
    PRESCRIPTION_ID   CHAR(6) NOT NULL,
    FOREIGN KEY (MEDICAL_RECORD_ID) REFERENCES MEDICAL_RECORD(MEDICAL_RECORD_ID),
    FOREIGN KEY (PRESCRIPTION_ID)   REFERENCES PRESCRIPTION(PRESCRIPTION_ID)
);

